import React, { useState} from "react";
import styled from "styled-components";
import { ToastContainer, toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import { users } from "../db/user.js";
import { login } from "../actions/isLoggedAction";
import { useDispatch } from "react-redux";
import { setUser } from "../actions/currentUser.js";

const Login = () => {
  const dispatch = useDispatch();
  const [values, setValues] = useState({ username: "", password: "" });
  const toastOptions = {
    position: "bottom-right",
    autoClose: 8000,
    pauseOnHover: true,
    draggable: true,
    theme: "dark",
  };

  const handleChange = (event) => {
    setValues({ ...values, [event.target.name]: event.target.value });
  };

  const validateForm = () => {
    const { username, password } = values;
    if (username === "") {
      toast.error("Email and Password is required.", toastOptions);
      return false;
    } else if (password === "") {
      toast.error("Email and Password is required.", toastOptions);
      return false;
    }
    return true;
  };

  const handleSubmit = async (event) => {
    event.preventDefault();
    if (validateForm()) {
      for (var i = 0; i <= users.length - 1; i++) {
        console.log(users[i].username)
        if (users[i].username === values.username) {
          if (users[i].password === values.password) {
            toast("Login Successfull", toastOptions);
            dispatch(login());
            dispatch(setUser(users[i]));
            break;
          } else {
            toast.error("Inccorect Password", toastOptions);
            break;
          }
        } else {
          toast.error("Users doesen't exist", toastOptions);
          break;
        }
  
      }
    }
     
   
  };

  return (
    <>
      <FormContainer>
        <form action="" onSubmit={(event) => handleSubmit(event)}>
          <div className="brand">
            <h1>DISHPOLL</h1>
          </div>
          <input
            type="text"
            placeholder="Username"
            name="username"
            onChange={(e) => handleChange(e)}
            min="3"
          />
          <input
            type="password"
            placeholder="Password"
            name="password"
            onChange={(e) => handleChange(e)}
          />
          <button type="submit">Log In</button>
        </form>
      </FormContainer>
      <ToastContainer />
    </>
  );
};

